[pinyin_by_len.setdefault(len(w),set()).add(w)
                for w in sorted (pinyin, key = lambda x : -len(x))]

def known (word) :
  lw = len(word)

  # Any combination.  Too many false positives
  # Perhaps try without syllables like "a", "aa", "ya"
  # Perhaps try requiring that at least one syllable be long.
  #for ln in by_len :
  #  if ln < lw and word[0:ln] in by_len[ln] :
  #    return known (word[ln:])
  #  elif ln == lw and word in by_len[ln] :
  #    return True

  # Words of up to two Chinese syllables
  for ln in pinyin_by_len :
    if (ln == lw and word in pinyin_by_len[ln]
        or ln < lw and (word[0:ln] in pinyin_by_len[ln]
                        and (lw-ln in pinyin_by_len)
                        and word[ln:] in pinyin_by_len[lw-ln])) :
      return True

  return False

